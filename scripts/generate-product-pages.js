// ---------------------------------------------------------------- utilities
function esc(s) {
  return String(s == null ? '' : s).replace(/[&<>"']/g, (c) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' }[c]));
}
function escXml(s) {
  return String(s).replace(/[&<>]/g, (c) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;' }[c]));
}
function money(v) {
  return v == null ? '$—' : v.toLocaleString('en-US', { style: 'currency', currency: 'USD' });
}
function perGb(v) {
  return '$' + (v >= 1 ? v.toFixed(2) : v.toFixed(3)) + '/GB';
}
function monthYear(iso) {
  return new Date(iso).toLocaleDateString('en-US', { month: 'short', year: 'numeric', timeZone: 'UTC' });
}
function longDate(iso) {
  return new Date(iso).toLocaleDateString('en-US', { month: 'long', day: 'numeric', year: 'numeric', timeZone: 'UTC' });
}
const round2 = (x) => Math.round(x * 100) / 100;
const median = (xs) => {
  const s = [...xs].sort((a, b) => a - b);
  const m = Math.floor(s.length / 2);
  return s.length % 2 ? s[m] : (s[m - 1] + s[m]) / 2;
};

// ------------------------------------------------------------- name parsing
// Same conventions as frontend/js/product-listing.js (kit-total capacity,
// TBW/Gb-s exclusion, SATA-first) — duplicated here because that file is
// browser-side; keep the two in sync when rules change.
function capTokensGB(str) {
  const caps = []; let m; const re = /(\d+)\s*(gb|tb)(?![\w/])/gi;
  while ((m = re.exec(str))) caps.push(/tb/i.test(m[2]) ? +m[1] * 1024 : +m[1]);
  return caps;
}
function totalCapacityGB(name) {
  const pre = capTokensGB(name.split('(')[0]);
  if (pre.length) return Math.max(...pre);
  const all = capTokensGB(name);
  return all.length ? Math.max(...all) : null;
}
function capacityLabel(gb) {
  if (gb == null) return null;
  return gb >= 1024 && gb % 1024 === 0 ? (gb / 1024) + 'TB' : gb + 'GB';
}
function parseSpeed(name) {
  const s = []; let m;
  const re = /(\d{4,5})\s*(?:mhz|mt\/s)/gi;
  while ((m = re.exec(name))) s.push(+m[1]);
  const re2 = /ddr[45]-(\d{4,5})/gi;
  while ((m = re2.exec(name))) s.push(+m[1]);
  const ok = s.filter((x) => x >= 1800 && x <= 9000);
  return ok.length ? Math.max(...ok) : null;
}
function ramType(name) {
  if (/ddr5/i.test(name)) return 'DDR5';
  if (/ddr4/i.test(name)) return 'DDR4';
  return null;
}
function ssdType(name) {
  if (/sata|2\.5/i.test(name)) return 'SATA';
  if (/nvme|m\.2/i.test(name)) return 'NVMe';
  return null;
}
function formFactor(name) {
  if (/m\.2/i.test(name)) return 'M.2';
  if (/2\.5/.test(name)) return '2.5"';
  return null;
}
function latency(name) {
  const m = name.match(/\bCL\s?(\d{2})\b/i);
  return m ? 'CL' + m[1] : null;
}

// ------------------------------------------------------------------- slugs
// Short display name: brand + the meaningful prefix of the title (up to the
// first "(" or comma), trimmed at a word boundary. Used for slugs and <title>.
function shortName(p) {
  let base = p.name.split('(')[0].split(',')[0].trim();
  if (p.brand && !base.toLowerCase().startsWith(p.brand.toLowerCase().slice(0, 4))) {
    base = p.brand + ' ' + base;
  }
  const words = base.split(/\s+/).slice(0, 8).join(' ');
  let out = words;
  if (out.length > 42) {
    out = out.slice(0, 42);
    out = out.slice(0, out.lastIndexOf(' ')); // word boundary
  }
  return out;
}
function computeSlug(p) {
  let src = shortName(p);
  const cap = capacityLabel(totalCapacityGB(p.name));
  const spd = p.category === 'ram' ? parseSpeed(p.name) : null;
  if (cap && !src.toLowerCase().includes(cap.toLowerCase())) src += ' ' + cap;
  if (spd && !src.includes(String(spd))) src += ' ' + spd + 'mhz';
  let slug = src.toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');
  if (slug.length > 80) {
    slug = slug.slice(0, 80);
    if (slug.includes('-')) slug = slug.slice(0, slug.lastIndexOf('-'));
  }
  return slug;
}

// --------------------------------------------------------------- data load
async function pagedSelect(build) {
  const out = [];
  for (let from = 0; ; from += PAGE) {
    const { data, error } = await build().range(from, from + PAGE - 1);
    if (error) throw error;
    out.push(...data);
    if (data.length < PAGE) break;
  }
  return out;
}

// Daily series: last reading of each UTC day, chronological.
function buildDailySeries(rows) {
  const byDay = new Map();
  for (const r of rows) {
    const day = r.fetched_at.slice(0, 10);
    const prev = byDay.get(day);
    if (!prev || r.fetched_at > prev.fetched_at) byDay.set(day, r);
  }
  return [...byDay.entries()]
    .map(([day, r]) => ({ day, price: Number(r.price), inStock: r.in_stock, fetched_at: r.fetched_at }))
    .sort((a, b) => (a.day < b.day ? -1 : 1));
}

// Chart downsample: daily within the last year, weekly 1-3 years back,
// monthly beyond 3 years. Keeps a decade-long history under ~500 points.
function downsampleForChart(series) {
  const now = Date.now();
  const oneYr = now - 365 * DAY_MS;
  const threeYr = now - 3 * 365 * DAY_MS;
  const out = [];
  let lastKey = null;
  for (const pt of series) {
    const t = new Date(pt.day + 'T00:00:00Z').getTime();
    let key;
    if (t >= oneYr) key = pt.day; // daily
    else if (t >= threeYr) key = pt.day.slice(0, 4) + '-w' + Math.floor(t / (7 * DAY_MS)); // weekly
    else key = pt.day.slice(0, 7); // monthly
    if (key === lastKey) out[out.length - 1] = [pt.day, pt.price]; // last of bucket wins
    else out.push([pt.day, pt.price]);
    lastKey = key;
  }
  return out;
}

function computeStats(series) {
  if (!series.length) return null;
  const now = Date.now();
  const current = series[series.length - 1];
  let atl = series[0], ath = series[0];
  for (const pt of series) {
    if (pt.price < atl.price) atl = pt;
    if (pt.price > ath.price) ath = pt;
  }
  const in90 = series.filter((pt) => new Date(pt.day).getTime() >= now - 90 * DAY_MS);
  const avg90 = in90.length ? round2(in90.reduce((a, p) => a + p.price, 0) / in90.length) : null;
  const target30 = now - 30 * DAY_MS;
  let base30 = null, bestDist = Infinity;
  for (const pt of series) {
    const t = new Date(pt.day).getTime();
    if (t >= now - 35 * DAY_MS && t <= now - 25 * DAY_MS) {
      const d = Math.abs(t - target30);
      if (d < bestDist) { bestDist = d; base30 = pt; }
    }
  }
  const change30 = base30 ? round2(((current.price - base30.price) / base30.price) * 100) : null;
  return {
    current: current.price,
    currentDay: current.day,
    atl: { price: atl.price, day: atl.day },
    ath: { price: ath.price, day: ath.day },
    avg90,
    change30,
    firstDay: series[0].day,
    days: series.length,
    limited: series.length < 30,
  };
}

// --------------------------------------------------------------- page HTML
function segmentLabel(seg) {
  return { ddr5: 'DDR5', ddr4: 'DDR4', nvme_ssd: 'NVMe SSD', sata_ssd: 'SATA SSD' }[seg] || seg;
}

function specRows(p) {
  const rows = [];
  const cap = capacityLabel(totalCapacityGB(p.name));
  if (p.category === 'ram') {
    const t = ramType(p.name);
    if (t) rows.push(['Type', t]);
    if (cap) rows.push(['Capacity', cap]);
    const spd = parseSpeed(p.name);
    if (spd) rows.push(['Speed', spd + 'MHz']);
    const cl = latency(p.name);
    if (cl) rows.push(['Latency', cl]);
  } else {
    const t = ssdType(p.name);
    if (t) rows.push(['Type', t]);
    if (cap) rows.push(['Capacity', cap]);
    const ff = formFactor(p.name);
    if (ff) rows.push(['Form Factor', ff]);
  }
  if (p.brand) rows.push(['Brand', p.brand]);
  return rows;
}

function specSummary(p) {
  const bits = [];
  const cap = capacityLabel(totalCapacityGB(p.name));
  if (cap) bits.push(cap);
  if (p.category === 'ram') {
    const t = ramType(p.name), spd = parseSpeed(p.name), cl = latency(p.name);
    if (t && spd) bits.push(t + '-' + spd);
    else if (t) bits.push(t);
    if (cl) bits.push(cl);
  } else {
    const t = ssdType(p.name), ff = formFactor(p.name);
    if (t) bits.push(t);
    if (ff) bits.push(ff);
  }
  return bits.join(' · ');
}

