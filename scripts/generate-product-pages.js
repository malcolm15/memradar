  const rowsByProduct = new Map();
  for (const r of allRows) {
    if (!rowsByProduct.has(r.product_id)) rowsByProduct.set(r.product_id, []);
    rowsByProduct.get(r.product_id).push(r);
  }

  // Per-product series + stats
  const problems = [];
