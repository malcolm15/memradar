function buildPage(template, ctx) {
  // Split template: head / body-before-main / after-main-to-scripts
  const headMatch = template.match(/<head>[\s\S]*?<\/head>/);
  const mainOpen = template.indexOf('<main>');
  const mainClose = template.indexOf('</main>');
  const chartTagIdx = template.indexOf('<script src="https://cdnjs.cloudflare.com/ajax/libs/Chart.js');
  const bodyEndIdx = template.indexOf('</body>');
  if (!headMatch || mainOpen < 0 || mainClose < 0 || chartTagIdx < 0 || bodyEndIdx < 0) {
    throw new Error('template anchors missing (head/main/Chart.js/body) — template drift, aborting');
  }

  const head = buildHead(headMatch[0], ctx);
  const beforeMain = template.slice(template.indexOf('</head>') + '</head>'.length, mainOpen);
  const afterMain = template.slice(mainClose + '</main>'.length, template.indexOf('<script src="../../js/main.js'));

  return `<!DOCTYPE html>
<html lang="en">
${head}
${MARKER}
<body>${beforeMain}<main>
${buildMain(ctx)}
  </main>${afterMain}${buildScripts(ctx)}
</body>
</html>
`;
}

// ------------------------------------------------------------------ sitemap
function buildSitemap(existingXml, productEntries, buildDate) {
  const staticEntries = [];
  const re = /<url>[\s\S]*?<\/url>/g;
  let m;
  while ((m = re.exec(existingXml))) {
    const loc = (m[0].match(/<loc>([^<]*)<\/loc>/) || [])[1] || '';
    if (!/^https:\/\/memradar\.com\/(ram|ssd)\/.+\/$/.test(loc)) staticEntries.push(m[0]);
  }
  const productXml = productEntries.map((u) => `  <url>
    <loc>${escXml(u)}</loc>
    <lastmod>${buildDate}</lastmod>
    <changefreq>daily</changefreq>
    <priority>0.6</priority>
  </url>`);
  return `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${staticEntries.map((e) => '  ' + e).join('\n')}
${productXml.join('\n')}
</urlset>
`;
}

